#include <iostream>
#include <vector>
using namespace std;

int kInversePairs(int n, int k)
{
    vector<vector<int>> dpTable(k + 1, vector<int>(n, 0));
    for (int i = 0; i <= k; i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i == 0)
                dpTable[i][j] = 1;
            else if (j == 0)
                dpTable[i][j] = 0;
            else
            {
                for (int k = 0; k <= min(i, j); k++)
                    dpTable[i][j] = (dpTable[i][j] + dpTable[i - k][j - 1]) % 1000000007;
            }
        }
    }

    return dpTable[k][n - 1];
}

int main()
{
    int n, k;
    cin >> n >> k;
    cout << kInversePairs(n, k) << endl;

    return 0;
}

